"""Trace-level deterministic graders for legislative content extraction agent.

This module evaluates tool-use trajectories for legislative content extraction
agent runs, checking PDF read behavior and HTML fetch appropriateness.

Examples
--------
>>> from aieng.agent_evals.legislative_content_extraction.graders import (
...     trace_deterministic_grader,
... )
>>> from aieng.agent_evals.legislative_content_extraction.task import (
...     LegislativeExtractionTask,
... )
>>> from aieng.agent_evals.evaluation import run_experiment_with_trace_evals
>>> task = LegislativeExtractionTask(files_dir="/path/to/files")
>>> results = run_experiment_with_trace_evals(
...     # <YOUR_DATASET_NAME>,
...     name="legislative_trace_level_demo",
...     task=task,
...     evaluators=[],
...     trace_evaluators=[trace_deterministic_grader],
... )
"""

from collections.abc import Mapping
from typing import Any

from aieng.agent_evals.evaluation import Evaluation, ExperimentItemResult
from aieng.agent_evals.evaluation.trace import _default_tool_call_predicate
from langfuse.api import ObservationsView, TraceWithFullDetails


def _get_field(payload: Any, key: str) -> Any:
    """Read a field from a dict-like or object payload."""
    if isinstance(payload, Mapping):
        return payload.get(key)
    return getattr(payload, key, None)


def trace_deterministic_grader(
    *, trace: TraceWithFullDetails, item_result: ExperimentItemResult, **kwargs: Any
) -> list[Evaluation]:
    """Evaluate one legislative content extraction trace for tool-use discipline.

    Parameters
    ----------
    trace : TraceWithFullDetails
        Langfuse trace containing observations generated by the agent run.
    item_result : ExperimentItemResult
        Experiment item result associated with ``trace``. Used to read
        ``should_use_html`` from item metadata.
    **kwargs : Any
        Additional keyword arguments forwarded by the harness (unused).

    Returns
    -------
    list[Evaluation]
        Trace metrics:

        - ``trace_read_pdf_call_count`` — number of ``read_pdf`` tool calls.
        - ``trace_fetch_html_appropriateness`` — 1.0 if the agent's HTML
          fetch decision matches the ground truth, 0.0 otherwise.
    """
    tool_observations: list[ObservationsView] = [
        obs for obs in (trace.observations or []) if _default_tool_call_predicate(obs)
    ]

    # Count read_pdf calls
    read_pdf_count = sum(
        1 for obs in tool_observations if "read_pdf" in (obs.name or "").lower()
    )

    # Check if fetch_html was called
    html_was_called = any(
        "fetch_html" in (obs.name or "").lower() for obs in tool_observations
    )

    # Read should_use_html from item metadata
    item = item_result.item
    item_metadata = _get_field(item, "metadata")
    should_use_html: bool = bool(_get_field(item_metadata, "should_use_html")) if item_metadata is not None else False

    html_appropriateness = 1.0 if html_was_called == should_use_html else 0.0

    return [
        Evaluation(name="trace_read_pdf_call_count", value=read_pdf_count),
        Evaluation(
            name="trace_fetch_html_appropriateness",
            value=html_appropriateness,
            metadata={
                "html_was_called": html_was_called,
                "should_use_html": should_use_html,
            },
        ),
    ]


__all__ = ["trace_deterministic_grader"]
