"""Trace-level deterministic graders for legislative content extraction agent.

This module evaluates tool-use trajectories for legislative content extraction
agent runs, checking per-invocation tool call counts for ``read_pdf``,
``validate_json``, and ``fetch_html``.

Scores are binary (0 or 1) **per-invocation** so that automatic retries
(which create separate "invocation" CHAIN observations) do not inflate counts.

Examples
--------
>>> from aieng.agent_evals.legislative_content_extraction.graders import (
...     trace_deterministic_grader,
... )
>>> from aieng.agent_evals.legislative_content_extraction.task import (
...     LegislativeExtractionTask,
... )
>>> from aieng.agent_evals.evaluation import run_experiment_with_trace_evals
>>> task = LegislativeExtractionTask(files_dir="/path/to/files")
>>> results = run_experiment_with_trace_evals(
...     # <YOUR_DATASET_NAME>,
...     name="legislative_trace_level_demo",
...     task=task,
...     evaluators=[],
...     trace_evaluators=[trace_deterministic_grader],
... )
"""

from typing import Any

from aieng.agent_evals.evaluation import Evaluation, ExperimentItemResult
from aieng.agent_evals.evaluation.trace import _default_tool_call_predicate
from langfuse.api import ObservationsView, TraceWithFullDetails


def _find_invocations(observations: list[ObservationsView]) -> list[ObservationsView]:
    """Return observations that represent top-level agent invocations.

    Each retry attempt creates a new ``invocation [<agent>]`` CHAIN observation
    under the root experiment-item-run span.
    """
    return [
        obs
        for obs in observations
        if (obs.type or "").upper() == "CHAIN"
        and "invocation" in (obs.name or "").lower()
    ]


def _map_obs_to_invocation(
    observations: list[ObservationsView],
    invocations: list[ObservationsView],
) -> dict[str, str]:
    """Map each observation ID to its enclosing invocation ID.

    Walks the ``parentObservationId`` chain for every observation until it
    reaches an invocation node, then caches the result.
    """
    invocation_ids = {obs.id for obs in invocations}
    obs_by_id = {obs.id: obs for obs in observations}
    cache: dict[str, str | None] = {}

    def _walk(obs_id: str) -> str | None:
        if obs_id in cache:
            return cache[obs_id]
        if obs_id in invocation_ids:
            cache[obs_id] = obs_id
            return obs_id
        obs = obs_by_id.get(obs_id)
        if obs is None or obs.parent_observation_id is None:
            cache[obs_id] = None
            return None
        result = _walk(obs.parent_observation_id)
        cache[obs_id] = result
        return result

    return {obs.id: inv_id for obs in observations if (inv_id := _walk(obs.id)) is not None}


def _last_invocation(invocations: list[ObservationsView]) -> ObservationsView | None:
    """Return the chronologically last invocation (the successful retry)."""
    if not invocations:
        return None
    return max(invocations, key=lambda obs: obs.start_time or "")


def trace_deterministic_grader(
    *, trace: TraceWithFullDetails, item_result: ExperimentItemResult, **kwargs: Any
) -> list[Evaluation]:
    """Evaluate one legislative content extraction trace for tool-use discipline.

    Scores are scoped to individual invocations so that automatic retries
    (empty-response recovery) do not inflate counts.

    Parameters
    ----------
    trace : TraceWithFullDetails
        Langfuse trace containing observations generated by the agent run.
    item_result : ExperimentItemResult
        Experiment item result associated with ``trace``.
    **kwargs : Any
        Additional keyword arguments forwarded by the harness (unused).

    Returns
    -------
    list[Evaluation]
        Trace metrics (binary 0/1):

        - ``trace_pdf_tool_correctness`` — 1 if ``read_pdf`` called exactly
          once in the last invocation, else 0.
        - ``trace_validate_tool_correctness`` — 1 if ``validate_json``
          called 1–2 times in the last invocation, else 0.
        - ``trace_html_tool_correctness`` — 1 if ``fetch_html`` called
          exactly once in the last invocation, else 0.
    """
    all_observations: list[ObservationsView] = list(trace.observations or [])
    tool_observations: list[ObservationsView] = [
        obs for obs in all_observations if _default_tool_call_predicate(obs)
    ]

    invocations = _find_invocations(all_observations)
    num_invocations = len(invocations)

    # Scope tool-call counts to the last (successful) invocation only.
    last_inv = _last_invocation(invocations)
    if invocations and last_inv is not None:
        obs_to_inv = _map_obs_to_invocation(all_observations, invocations)
        last_tools = [
            obs for obs in tool_observations if obs_to_inv.get(obs.id) == last_inv.id
        ]
    else:
        last_tools = tool_observations

    read_pdf_count = sum(1 for obs in last_tools if "read_pdf" in (obs.name or "").lower())
    validate_json_count = sum(1 for obs in last_tools if "validate_json" in (obs.name or "").lower())
    fetch_html_count = sum(1 for obs in last_tools if "fetch_html" in (obs.name or "").lower())

    # Binary scores: 1 = correct tool-use behaviour, 0 = incorrect.
    pdf_score = 1 if read_pdf_count == 1 else 0
    validate_score = 1 if 1 <= validate_json_count <= 2 else 0
    html_score = 1 if fetch_html_count == 1 else 0

    return [
        Evaluation(
            name="trace_pdf_tool_correctness",
            value=pdf_score,
            metadata={
                "actual_count": read_pdf_count,
                "num_invocations": num_invocations,
            },
        ),
        Evaluation(
            name="trace_validate_tool_correctness",
            value=validate_score,
            metadata={
                "actual_count": validate_json_count,
                "num_invocations": num_invocations,
            },
        ),
        Evaluation(
            name="trace_html_tool_correctness",
            value=html_score,
            metadata={
                "actual_count": fetch_html_count,
                "num_invocations": num_invocations,
            },
        ),
    ]


__all__ = ["trace_deterministic_grader"]
